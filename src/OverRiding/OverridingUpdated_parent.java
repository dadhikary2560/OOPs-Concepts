package OverRiding;

public class OverridingUpdated_parent {
	public OverridingUpdated_parent() {
		// constructor of the parent class
	}
	protected void m1() {
		System.out.println("parent method m1");
	}

	void m2() {
		System.out.println("this is parent method m2");
	}
	static void m3() {
		System.out.println("this is static method of parent class");
	}
}
